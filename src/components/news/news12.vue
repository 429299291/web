    <p>玻尿酸，原名透明质酸（化学式C28H44N2O23），是一种高分子的多醣体，质地透明、具黏性。它是人体真皮组织的成分之一，具有表皮组织修复的功能，还能深层保湿、增加皮肤弹性与张力。</p>
    <p>随着年龄增长和机体老化，人体玻尿酸逐渐开始流失，会让皮肤变差。据说，如果把20岁时人体玻尿酸含量看作100%，那么，30、50、60岁时就分别只有65%、45%、25%</p>
    <p>早在1934年，科学家们就从牛眼、鸡冠中提取出来玻尿酸，但工艺十分复杂。</p>
    <img src="https://mpt.135editor.com/mmbiz_jpg/FgGdI1xiarFmaGxpoz8eayiazsVtzo54gksFdrvWliazSr6tazxDg6r5fWpNyo6LPxaYsnJhGlpR6LKykMBEqicO5Q/640?wx_fmt=jpeg" alt="">
    <p>到了1985年，日本人第一次成功采用微生物培养出玻尿酸。开始也主要运用于眼科手术的辅助剂，后来又因它具有抑制炎症、润滑等作用，骨科方面也开始使用。</p>
    <h3>2. 赚钱，还是要女人爱上</h3>
    <p>现代高消费群体排行：女人、孩子、老人、狗、男人。</p>
