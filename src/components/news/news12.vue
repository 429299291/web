    <p>而根据2017年，一家做玻尿酸溶液厂家发布的招股材料显示，一针玻尿酸，主要有三种原材料，透明质酸钠、注射器、针头。</p>
    <p>经过计算，三种不同产品成本在44.79~113.67元，售价高达368.01~2530.37元，毛利最高可达98.23%！</p>
