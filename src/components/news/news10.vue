    <p>这些都还是小困难，真正的困难是，你作为一名合格的黑帮，如何在如此严峻的形式下搞到钱，你要整点活儿，这才是最重要的。</p>
    <em>这群黑社会大哥，为了谋生计绞尽了脑汁。</em>
    <p>比方说，有日媒也爆料称，日本第六代山口组分支组长坂上明弘和店内几名黑帮成员分工明确，开了家居酒屋。</p>
    <p>这里的海鲜饭只要500日元，不仅价格便宜而且用料十足、味道鲜美，为居酒屋带来了庞大的客流与收入。</p>
    <p>结果被同行举报后，警察最终发现这里的鱼都是非法捕捞。</p>
