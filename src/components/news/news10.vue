    <p>但现在，山口组高层更换了入行测试，他们根据最新的《排除暴力团伙条例》所出的“防坐牢测试”，这是一套长达十二页纸的试卷。</p>
    <p>试题包括“什么样的行为是被法律禁止的？”“如何倾倒工业垃圾？”等等。</p>
