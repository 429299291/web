    <h3>01</h3>
    <p>昨天看到一个新闻。</p>
    <p>说是日本《神户新闻》4日报道，日本警方抓获了一名犯罪嫌疑人，名叫中田浩司，他是日本黑帮“神户山口组”核心人物，任“山健组”组长。</p>
    <p>他犯的事，是在今年8月21日，试图射杀敌对帮派“山口组”的一名黑帮成员，最终是没有打中，造成对方重伤。</p>
    <img src="http://zkres1.myzaker.com/201912/5dede9598e9f09430e1d0415_1024.jpg" alt="">
    <p>黑帮火拼从小在电视电影里就看，从小栗旬到陈浩南，从《艋舺》到《新世界》，内心已经毫无波澜了，可当我看到他们年龄的时候，我对于黑帮的幻想沉底破灭了。</p>
    <p>这个被袭击的大叔已经五十一岁了，而被抓的那个大叔（大爷？）已经六十岁了。</p>
